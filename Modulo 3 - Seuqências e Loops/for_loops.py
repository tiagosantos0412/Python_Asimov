# Montando uma estrutura de repetição de 0 a 9
for i in range(10):
    print(f'O índice agora vale {i}')

# Montando uma estrutura de repetição de 1 a 10
for i in range(1,11):
    print(f'O índice agora vale {i}')

# Tabuada
numero = 8
for i in range (1,11):
    print(f'{i} X {numero} = {numero * i}')

